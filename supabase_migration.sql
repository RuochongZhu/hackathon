DROP VIEW IF EXISTS public.joined_readmission_dataset;

CREATE VIEW public.joined_readmission_dataset AS
SELECT
    e.preset,
    e.encounter_id,
    p.patient_id,
    p.age,
    p.sex,
    p.chronic_conditions_count,
    p.diagnosis_group,
    p.insurance_type,
    p.living_alone,
    p.primary_language,
    e.admit_date,
    e.discharge_date,
    e.length_of_stay,
    e.severity_score,
    e.icu_flag,
    e.prior_admissions_12m,
    d.discharge_disposition,
    d.followup_scheduled,
    d.medication_complexity,
    d.transportation_barrier,
    d.missed_appointments_history,
    v.last_temp,
    v.last_oxygen_sat,
    v.last_systolic_bp,
    v.last_heart_rate,
    v.abnormal_vitals_flag,
    e.readmitted_30d
FROM public.encounters e
JOIN public.patients p ON e.patient_id = p.patient_id AND e.preset = p.preset
JOIN public.discharge_factors d ON e.encounter_id = d.encounter_id AND e.preset = d.preset
JOIN public.vitals_summary v ON e.encounter_id = v.encounter_id AND e.preset = v.preset;
